#Looping - Executes as long as a condition is meet.

#For loops - Start to finish of an iterate.
vegtables = ["Cucumber", "Tomato", "Bell Pepper"]

for x in vegtables:
	print(x)


#while loops - execute as long as true
i = 1

while i < 10:
	print(i)
	i += 1


input("Press Enter to continue...")